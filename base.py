import sqlite3
import os
from dotenv import load_dotenv

# Загружаем переменные окружения
load_dotenv()

# Получаем имя базы данных из переменных окружения
DATABASE_NAME = os.getenv('DATABASE_NAME', 'sqlite_python.db')


def bot_gribs(id, name, wt, cash, topic, photo, description):
    try:
        sqlite_connection = sqlite3.connect(DATABASE_NAME)
        cursor = sqlite_connection.cursor()
        print("Подключен к SQLite")
        cursor.execute("""CREATE TABLE IF NOT EXISTS list_gribs
            (
                id INTEGER PRIMARY KEY,
                name TEXT NOT NULL,
                wt TEXT NOT NULL,
                cash INT NOT NULL,
                topic TEXT NOT NULL,
                photo TEXT NULL,
                description TEXT NULL
            )
        """)
        sqlite_connection.commit()

        sqlite_insert_blob_query = """INSERT OR REPLACE INTO list_gribs
                                  (id, name, wt, cash, topic, photo, description) VALUES (?, ?, ?, ?, ?, ?, ?)"""

        # Преобразование данных в формат кортежа
        data_tuple = (id, name, wt, cash, topic, photo, description)
        cursor.execute(sqlite_insert_blob_query, data_tuple)
        sqlite_connection.commit()
        print("Данные успешно загружены в таблицу")
        cursor.close()

    except sqlite3.Error as error:
        print("Ошибка при работе с SQLite", error)
    finally:
        if sqlite_connection:
            sqlite_connection.close()


def delete_product_by_id(product_id):
    """Удаляет товар из базы данных по ID"""
    try:
        sqlite_connection = sqlite3.connect(DATABASE_NAME)
        cursor = sqlite_connection.cursor()
        cursor.execute("DELETE FROM list_gribs WHERE id=?", (product_id,))
        sqlite_connection.commit()
        print(f"Товар с ID {product_id} успешно удален из базы данных")
        cursor.close()
    except sqlite3.Error as error:
        print(f"Ошибка при удалении товара с ID {product_id}:", error)
    finally:
        if sqlite_connection:
            sqlite_connection.close()


